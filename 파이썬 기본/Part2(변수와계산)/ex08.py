# 내장 함수 및 math라이브러리 함수 이용

from math import * # 라이브러리 가져올 때, 한 번만 코딩하면 됨

# 절대값 구하기
print(abs(-77))
print(abs(77))

# 반올림 하기
print(round(1.222))
print(round(1.5222))

# 주어진 매개변수 값 중에서 최대값 구하기
print(max(10, -20, 100, 9999))

# 주어진 매개변수 값 중에서 최소값 구하기
print(min(10, -20, 100, 9999))

# 4.0의 제곱근을 구하기
print(sqrt(4.0))

print(pow(3, 2))